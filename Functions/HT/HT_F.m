gam = 0.0307;



P = (log10(log10(8.122e6 * mu + 0.8)) - C1) / n;

T = 2 * (10^P - 560);

Ef = 9336 * Q * gam * C * T;

h = (((2 * pi * N) / 60)^2) * ((2 * pi * mu) / Ef) * (((R^4) / 4) - ((R0^4) / 4));

